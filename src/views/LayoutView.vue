<script>
import gsap from "gsap";

import API from "../api/index";

export default {
  components: {},
  data() {
    return {
      items: [],
      columns: [],
      counter: 0,
      max: -1,
      showMax: 3,
      // actualGame: "Bonus Game 1 - Metal Gear Solid 2: Substance",
      actualGame: "",
      actualRunner: "",
      actualTime: "",
      timeline: gsap.timeline({
        repeat: -1,
        repeatDelay: 10,
      }),
      tiempos: [2, 10, 3, 4],
      socials: [
        {
          name: "twitter",
          img: "/assets/twitter.svg",
          social: "@SPEEDRUNESPANOL",
        },
        {
          name: "youtube",
          img: "/assets/youtube.svg",
          social: "SPEEDRUNSESPANOL",
        },
      ],
    };
  },
  sockets: {
    connect: function () {
      console.log("socket connected");
    },
    advance: function () {
      this.counter++;
      this.timeline.restart().clear();
      this.changeHoraro();
      this.startAnimation();
    },
    manualAdvance: function (counter) {
      this.counter = counter + 1;
      this.timeline.restart().clear();
      this.changeHoraro();
      this.startAnimation();
    },
    back: function () {
      this.counter--;
      this.timeline.restart().clear();
      this.changeHoraro();
      this.startAnimation();
    },
    getCounter(counter) {
      this.counter = counter;
    },
  },
  methods: {
    initialState() {
      //   height: 45px;
      //   height: 80px;
      this.timeline.set(".bar", { height: "45px" });
      this.timeline.set([".bienvenida", ".redes", ".list"], {
        display: "none",
        opacity: 0,
      });
      this.timeline.set([".social-img", ".social-text"], { opacity: 0 });
    },
    showGreetings() {
      let div = document.querySelector(".bienvenida");
      this.timeline
        .to(div, {
          duration: 1,
          display: "block",
          opacity: 1,
        })
        .to(div, {
          duration: 1,
          opacity: 0,
          delay: this.tiempos[1],
        })
        .set(div, { display: "none" });
      return true;
    },
    showSocials() {
      let div = document.querySelector(".redes");
      let img = document.querySelector(".social-img");
      let text = document.querySelector(".social-text");
      this.timeline.to(div, {
        duration: 1,
        display: "block",
        opacity: 1,
        delay: this.tiempos[1],
      });
      this.timeline
        .to([img, text], {
          duration: 1,
          display: "block",
          opacity: 1,
        })
        .to([img, text], {
          duration: 1,
          opacity: 0,
          delay: this.tiempos[1],
          onComplete: () => {
            img.src = this.socials[1].img;
            text.innerHTML = this.socials[1].social;
          },
        });
      this.timeline
        .to([img, text], {
          duration: 1,
          display: "block",
          opacity: 1,
        })
        .to([img, text], {
          duration: 1,
          opacity: 0,
          delay: this.tiempos[1],
        });
      this.timeline
        .to(div, {
          duration: 1,
          opacity: 0,
          delay: 1,
        })
        .set(div, { display: "none" });
      return true;
    },
    showNextRuns() {
      let div = document.querySelector(".list");
      let next = document.querySelector(".next");

      this.timeline
        .set(".list-texto", {
          x: "-15px",
        })
        .to(div, {
          duration: this.tiempos[0],
          display: "flex",
          opacity: 1,
          delay: 1,
        })
        .to(".list-texto", {
          duration: this.tiempos[2],
          x: "-743px",
          delay: this.tiempos[2],
        })
        .to(".bar", {
          duration: 1,
          height: "80px",
          delay: 1,
        })
        .to(next, {
          duration: this.tiempos[3],
          x: "-758px",
          delay: 1,
          ease: "Power2.easeInOut",
        })
        .to([".list-texto", next], {
          duration: 1,
          opacity: 0,
          delay: this.tiempos[1],
        });
      for (let i = 1; i < this.showMax; i++) {
        if (this.counter + i < this.max) {
          this.timeline
            .set(next, {
              x: "0px",
              opacity: 1,
              onComplete: () => {
                this.changeHoraro(this.counter + i);
              },
            })
            .to(next, {
              duration: this.tiempos[3],
              x: "-758px",
              delay: this.tiempos[2],
              ease: "Power2.easeInOut",
            })
            .to(next, {
              duration: 1,
              opacity: 0,
              delay: this.tiempos[1],
            });
        } else {
          break;
        }
      }
      this.timeline
        .set(next, {
          x: "0px",
          opacity: 1,
          onComplete: () => {
            this.changeHoraro(this.counter);
          },
        })
        .to(div, {
          duration: this.tiempos[0],
          opacity: 0,
          delay: 1,
        })
        .set(div, { display: "none" })
        .to(".bar", {
          duration: 1,
          height: "45px",
        });

      return true;
    },
    changeHoraro(count = this.counter) {
      if (count < this.max) {
        let index = this.columns.findIndex((element) => element == "Juego");
        this.actualGame = this.items[count].data[index];
        index = this.columns.findIndex((element) => element == "Runner/s");
        this.actualRunner = this.items[count].data[index];
        this.actualTime = this.toHHMMSS(this.items[count].length_t);
        return true;
      }
      return false;
    },
    alternateLogos() {
      let tl = gsap.timeline({
        repeat: -1,
        repeatDelay: 10,
      });

      tl.set("#sre", { opacity: 0 });
      tl.to("#esa", {
        duration: 1,
        opacity: 0,
        delay: 10,
      })
        .to(
          "#sre",
          {
            duration: 1,
            opacity: 1,
          },
          "-=1"
        )
        .to("#sre", {
          duration: 1,
          opacity: 0,
          delay: 10,
        })
        .to(
          "#esa",
          {
            duration: 1,
            opacity: 1,
          },
          "-=1"
        );
      return true;
    },
    toHHMMSS(sec_num) {
      let hours = Math.floor(sec_num / 3600).toString();
      let minutes = Math.floor(
        (sec_num - Number(hours) * 3600) / 60
      ).toString();
      let seconds = (
        sec_num -
        Number(hours) * 3600 -
        Number(minutes) * 60
      ).toString();

      if (Number(hours) < 10) {
        hours = "0" + hours;
      }
      if (Number(minutes) < 10) {
        minutes = "0" + minutes;
      }
      if (Number(seconds) < 10) {
        seconds = "0" + seconds;
      }
      return hours + ":" + minutes + ":" + seconds;
    },
    startAnimation() {
      this.initialState();
      this.showGreetings();
      this.showSocials();
      this.showNextRuns();
    },
  },
  created: async function () {
    this.$socket.emit("getCounter");
  },
  mounted: async function () {
    this.alternateLogos();
    let response = await API.getHoraro();
    this.items = response.data.items;
    this.max = this.items.length;
    this.columns = response.data.columns;
    this.changeHoraro();
    this.startAnimation();
    let interval = setInterval(async () => {
      let response = await API.getHoraro();
      this.items = response.data.items;
      this.max = this.items.length;
      this.columns = response.data.columns;
    }, 600000);
  },
};
</script>

