  <main>
    <div class="bg-container">
      <img class="bg-container-divisor" src="/assets/divisores.png" alt="" />
    </div>
    <div class="comentaristas-container">
      <h1 class="comentaristas-container-title">comentaristas</h1>
    </div>
    <div id="logos">
      <div id="esa">
        <img src="/assets/logo-esa-solo.png" />
      </div>
      <div id="sre">
        <img src="/assets/logo_plano.png" alt="sre_logo" />
      </div>
    </div>
    <div class="left-bar">
      <div class="left-bar-bg"></div>
    </div>
    <div class="bot-bar">
      <div class="bot-bar-bg"></div>
    </div>
    <div class="bg-bar">
      <div class="bar">
        <div class="bienvenida">
          <h1 class="bienvenida-texto">
            Retransmisión en español <span>ESA Summer 2022</span>
          </h1>
        </div>
        <div class="redes">
          <h1 class="redes-texto">
            Siguenos en:
            <span class="social-red">
              <img class="social-img" src="/assets/twitter.svg" alt="" />
              <span class="social-text">@SPEEDRUNESPANOL</span>
            </span>
          </h1>
        </div>
        <div class="list">
          <h1 class="list-texto">Ya viene:</h1>
          <div class="next">
            <h1 class="next-title">
              {{ actualGame }}
            </h1>
            <h2 class="next-runner">
              <font-awesome-icon class="icons" icon="running" />
              <span>{{ actualRunner }}</span>
            </h2>
            <h3 class="next-time">
              <font-awesome-icon class="icons" icon="clock" /><span>{{
                actualTime
              }}</span>
            </h3>
          </div>
        </div>
      </div>
    </div>
